#import "ButtonBarView.h"

@implementation ButtonBarView

- (id)initWithFrame:(CGRect)frame
{
    self = [super initWithFrame:frame];
    if (self) {
        [self setup];
    }
    return self;
}

-(void)awakeFromNib{
    [self setup];
}

-(void)setup{
    [self setBackgroundColor:BUTTON_BAR_COLOR];
}



@end
